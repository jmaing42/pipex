#include "ms_parse.h"

#include "wrap.h"

void	ms_parse_tokenize_free(t_ms_parse_token_list *list)
{
	while (list->head)
	{
		list->tail = list->head;
		list->head = list->head->next;
		wrap_free(list->tail->value.data);
		wrap_free(list->tail);
	}
}
