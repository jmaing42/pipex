#include "ms_parse.h"

void	ms_parse_skip_space_if_any(
	t_ms_parse_token_list_node **head
)
{
	if ((*head)->value.type == MS_PARSE_TOKEN_TYPE_SPACE)
		*head = (*head)->next;
}
